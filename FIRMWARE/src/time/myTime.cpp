// DESCRIPTION : Initialisation de la carte : GPIO, Clocks, Interruptions...
//--------------------------------------------------------------------------------------------------
void TIME_TaskInit(void)
{
    _ms = millis();
}

void TIME_TaskRun(void)
{
    if ((millis() - _ms) >= 60000)
    {
        _ms = millis();
        _nbMinutes++;
        if ( _nbMinutes >= 1440){
            _nbMinutes = 0;
        }
    }
}

unsigned int TIME_getMinutesSinceMidnight(void){
    return _nbMinutes;
}

void TIME_setMinutesSinceMidnight(unsigned int val){
    _ms = millis();
    _nbMinutes = val;
}
