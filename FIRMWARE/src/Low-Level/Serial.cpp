void SERIAL_Init(void)
{
    Serial.begin(115200);
    Serial.println();
}

uint16_t SERIAL_Available(void){
    return Serial.available();
}

uint8_t SERIAL_Getc(void){
    return Serial.read();
}

uint16_t SERIAL_PrintString(String str){
    Serial.print(str);
    return str.length();
}

uint16_t SERIAL_PrintC(char c){
    Serial.print(c);
    return 1;
}